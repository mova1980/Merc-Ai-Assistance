export function getCitationFormats(paper: Paper): CitationFormat {
  const authorsList = paper.authors.replace(/،/g, ".,").replace(/,/g, ".,") + ".";
  const yearStr = `(${paper.year})`;
  
  // APA formatted source
  const apa = `${authorsList} ${yearStr}. ${paper.title}. ${paper.journal}, ${paper.doi ? `https://doi.org/${paper.doi}` : ""}`;
  
  // Chicago formatted source
  const chicago = `${authorsList} "${paper.title}." ${paper.journal} (سال ${paper.year})${paper.doi ? `: doi:${paper.doi}` : ""}.`;

  // BibTeX formatted source
  const cleanKey = `merc_${paper.id.replace("-", "_")}_${paper.year}`;
  const bibtex = `@article{${cleanKey},
  author = {${paper.authors.replace(/،/g, " and ")},
  title = {${paper.title}},
  journal = {${paper.journal}},
  year = {${paper.year}},
  ${paper.doi ? `doi = {${paper.doi}},` : ""}
  keywords = {${paper.keywords.join(", ")}}
}`;

  // RIS formatted source
  const ris = `TY  - JOUR
AU  - ${paper.authors.replace(/، /g, "\nAU  - ")}
TI  - ${paper.title}
JO  - ${paper.journal}
PY  - ${paper.year}
${paper.doi ? `DO  - ${paper.doi}` : ""}
UR  - https://doi.org/${paper.doi || ""}
ER  - `;

  return { apa, chicago, bibtex, ris };
}
